import type { MongoClientOptions } from 'mongodb'
import type { ResolvedOptions } from '../../options'
import type { ResolvedMongoManagementOptions, ResolvedMongoOptions } from '../../options/database/mongodb'

import { klona } from 'klona/json'

export function getServerMongodbContents(options: ResolvedOptions) {
  return (): string => {
    const mongoUrl = options?.database?.mongo?.url
    const mongoOptions = klona(options?.database?.mongo) as Partial<ResolvedMongoOptions>
    const management = (mongoOptions.management || {}) as Partial<ResolvedMongoManagementOptions>
    delete mongoOptions.url
    delete mongoOptions.management

    const mongoClientOptions = mongoOptions as MongoClientOptions

    return `// ! Generated by nuxt-feathers-zod - do not change manually
// MongoDB infrastructure for embedded runtime (Feathers Dove style)

import { createMongoInfrastructure } from 'nuxt-feathers-zod/server-mongodb'

const mongodbConnection = ${JSON.stringify(mongoUrl)}
const mongodbOptions = ${JSON.stringify(mongoClientOptions, null, 2)}
const management = ${JSON.stringify(management, null, 2)}

export const mongodb = createMongoInfrastructure({
  mongodbConnection,
  mongodbOptions,
  management,
})

export default mongodb
`
  }
}

export function getServerMongodbTypesContents(_options: ResolvedOptions) {
  return () => `// ! Generated by nuxt-feathers-zod - do not change manually

declare module './server' {
  interface Configuration {
    mongodb?: string
    mongoPath?: string
    mongodbClient?: Promise<any>
    mongodbDb?: any
    mongodbConnection?: any
    currentDatabase?: string
    mongodb_ok?: boolean
  }
}
`
}
