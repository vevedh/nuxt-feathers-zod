import type { Import } from 'unimport'
import type { ResolvedOptions } from '../../options'
import type { DefaultAuthOptions } from '../../options/authentication'
import type { ClientOptions, ResolvedClientOptions } from '../../options/client'
import type { ServicesDirs } from '../../options/services'
import { scanDirExports } from 'unimport'
import { setImportsMeta } from '../../options/utils'
import { put } from '../utils'

async function getServices(servicesDirs: ServicesDirs): Promise<Import[]> {
  const services = await scanDirExports(servicesDirs, {
    filePatterns: ['**/*.shared.ts'],
    fileFilter: file => /\.shared\.ts$/.test(file),
    types: false,
  })
  return services.filter(({ name }) => /Client|default$/.test(name))
}

export function getClientPluginContents(options: ResolvedOptions) {
  return async (): Promise<string> => {
    // In remote mode, the client talks to an external Feathers backend.
    // We must NOT auto-import local generated "*.shared" service clients, because:
    // - they are unnecessary (services are external)
    // - they can introduce build-time coupling and TS issues in consumer apps
    // Embedded mode keeps the legacy behavior (scan + configure).
    const resolvedMode = (options.client as ResolvedClientOptions)?.mode
    const services = resolvedMode === 'remote'
      ? []
      : setImportsMeta(await getServices(options.servicesDirs))

    const plugins = (options.client as ResolvedClientOptions).plugins

    const modules = [...services, ...plugins]

    const moduleAuth = ((options?.auth as DefaultAuthOptions)?.authStrategies || []).length > 0
    const remoteAuthEnabled = Boolean((options?.client as ResolvedClientOptions)?.remote?.auth?.enabled)
    const auth = moduleAuth || remoteAuthEnabled
    const pinia = !!(options?.client as ClientOptions)?.pinia

    return `// ! Generated by nuxt-feathers-zod - do not change manually
// For more information about this file see https://dove.feathersjs.com/guides/cli/client.html

import { useCookie } from "#imports"
import type { ClientApplication } from './client'
import { feathers } from '@feathersjs/feathers'
import { defineNuxtPlugin, useRequestURL, useRuntimeConfig } from '#app'
import { buildRemoteAuthPayload } from 'nuxt-feathers-zod/auth-utils'
import { getPublicClientMode, getPublicRemoteAuthConfig, getPublicRemoteConfig } from 'nuxt-feathers-zod/config-utils'
${put(pinia, `import { createPiniaClient, type CreatePiniaClientConfig } from 'feathers-pinia'`)}

import { connection } from './connection'
${put(auth, `import { authentication } from './authentication'`)}

${modules.map(module => module.meta.import).join('\n')}

/**
 * Returns a typed client for the feathers-api app.
 *
 * @param connection The REST or Socket.io Feathers client connection
 * @param authenticationOptions Additional settings for the authentication client
 * @see https://dove.feathersjs.com/api/client.html
 * @returns The Feathers client application
 */
function createFeathersClient(): ClientApplication {
  const feathersClient: ClientApplication = feathers()

  // Init connection
  const runtime = useRuntimeConfig()
  const clientMode = getPublicClientMode(runtime.public as any)
  const remoteConfig = getPublicRemoteConfig(runtime.public as any)
  const remoteUrl = remoteConfig?.url
  const remoteRestPath = remoteConfig?.restPath
  const remoteWebsocketPath = remoteConfig?.websocketPath
  const remoteTransport = remoteConfig?.transport
  const remoteAuth = getPublicRemoteAuthConfig(runtime.public as any)
  const remoteServices = remoteConfig?.services
  const browserOrigin = import.meta.client && typeof window !== 'undefined' ? window.location.origin : ''
  const requestOrigin = import.meta.server ? useRequestURL().origin : ''
  const embeddedBaseUrl = browserOrigin || requestOrigin
  const baseUrl = clientMode === 'remote' ? (remoteUrl || embeddedBaseUrl) : embeddedBaseUrl
  const resolvedRemoteTransport = clientMode === 'remote' ? (remoteTransport || 'socketio') : remoteTransport
  const overrides = clientMode === 'remote'
    ? { mode: 'remote' as const, restPath: remoteRestPath, websocketPath: remoteWebsocketPath, transport: resolvedRemoteTransport }
    : { mode: clientMode, transport: resolvedRemoteTransport }

  // Official-ish remote pattern: create Feathers, then configure exactly one transport.
  feathersClient.configure(connection(baseUrl, overrides))

  // DX/runtime safety: normalize weird rejections into a stable plain object.
  const normalizeError = (err: any) => {
    const toPlain = (src: any, fallbackMessage = 'Unknown error') => ({
      name: src?.name || 'Error',
      message: src?.message || fallbackMessage,
      code: src?.code ?? src?.statusCode ?? src?.status ?? src?.name ?? 'NFZ_ERROR',
      className: src?.className,
      data: src?.data,
      errors: src?.errors,
      cause: src?.cause,
      stack: src?.stack ? String(src.stack) : undefined,
      raw: src,
    })

    if (err == null)
      return toPlain({ name: 'Error', message: 'Unknown error', code: 'NFZ_UNKNOWN' }, 'Unknown error')
    if (typeof err !== 'object')
      return toPlain({ name: 'Error', message: String(err), code: 'NFZ_ERROR', raw: err }, String(err))
    if (err instanceof Error)
      return toPlain(err, err.message || 'Error')
    return toPlain(err)
  }

  // Init authentication
  ${put(auth, `feathersClient.configure(authentication)`)}

  // Init remote services (manifest not required)
  // Official Feathers clients can use app.service(path) directly after transport configuration.
  // Only pre-register a service when explicit custom methods are declared.
  if (clientMode === 'remote' && Array.isArray(remoteServices) && remoteServices.length) {
    const conn: any = (feathersClient as any).get?.('connection')
    const standardMethods = new Set(['find', 'get', 'create', 'update', 'patch', 'remove'])
    for (const svc of remoteServices) {
      const path = String((svc as any)?.path || '').trim()
      if (!path) continue
      const methods = Array.isArray((svc as any).methods) ? (svc as any).methods.filter(Boolean) : []
      const hasCustomMethods = methods.some((m: any) => !standardMethods.has(String(m)))
      if (!hasCustomMethods)
        continue
      try {
        const remote = conn?.service ? conn.service(path) : undefined
        if (remote)
          ;(feathersClient as any).use(path, remote, { methods })
      }
      catch (e) {
        // ignore
      }
    }
  }

  if (import.meta.dev && clientMode === 'remote') {
    try {
      console.info('[NFZ CLIENT OFFICIAL]', {
        mode: clientMode,
        transport: resolvedRemoteTransport,
        apiType: piniaClient ? 'pinia-client' : 'feathers-client',
        socketCreated: Boolean((feathersClient as any).get?.('socket')),
        socketConnected: Boolean((feathersClient as any).get?.('socket')?.connected),
        remoteUrl,
        remoteRestPath,
        remoteWebsocketPath,
        remoteServices,
      })
    }
    catch {}
  }

  // Init services
  ${services.map(service => `feathersClient.configure(${service.meta.importId})`).join('\n  ')}

  // Init plugins
  ${plugins.map(plugin => `feathersClient.configure(${plugin.meta.importId})`).join('\n  ')}

  return feathersClient
}

/**
 * Creates a Feathers Rest client for the SSR server and a Socket.io client for the browser.
 * Also provides a cookie-storage adapter for JWT SSR using Nuxt APIs.
 */
export default defineNuxtPlugin(async (nuxt) => {
  // create the feathers client
  const feathersClient: ClientApplication = createFeathersClient()

  // Remote auth: normalize startup + Socket.IO reconnect behavior.
  const rc = useRuntimeConfig() as any
  const cm = getPublicClientMode(rc.public as any)
  const ra = getPublicRemoteAuthConfig(rc.public as any)

  async function resolveRemoteToken() {
    if (import.meta.server)
      return null

    const keycloak = (nuxt as any).$keycloak
    if (ra?.payloadMode === 'keycloak' && keycloak?.authenticated) {
      try {
        await keycloak.updateToken?.(30)
      }
      catch {}
      const kcToken = typeof keycloak?.token === 'function' ? keycloak.token() : keycloak?.token
      if (typeof kcToken === 'string' && kcToken)
        return kcToken
    }

    const storageKey = ra?.storageKey || rc.public?._feathers?.auth?.client?.storageKey || 'feathers-jwt'
    const jwt = useCookie<string | null>(storageKey)
    return jwt.value || null
  }

  async function remoteAuthenticate(reason: string) {
    if (import.meta.server || cm !== 'remote' || !ra?.enabled)
      return

    const api: any = feathersClient as any
    const token = await resolveRemoteToken()

    try {
      if (token) {
        const payload = buildRemoteAuthPayload(token, ra)
        await api.authenticate?.(payload)
        return
      }

      if (ra?.reauth !== false)
        await api.reAuthenticate?.()
    }
    catch (e) {
      // ignore startup/reconnect auth errors
    }
  }

  if (import.meta.client && cm === 'remote' && ra?.enabled) {
    await remoteAuthenticate('startup')

    const socket = (feathersClient as any).get?.('socket')
    if (socket?.on) {
      socket.on('connect', () => {
        void remoteAuthenticate('socket-connect')
      })
      socket.on('reconnect', () => {
        void remoteAuthenticate('socket-reconnect')
      })
    }
  }
  ${put(pinia, `
  const piniaOptions = useRuntimeConfig().public._feathers.pinia
  let piniaClient: any
  const wantsPiniaClient = piniaOptions !== false
  const hasPinia = !!(nuxt as any).$pinia

  if (wantsPiniaClient) {
    if (!hasPinia) {
      console.warn("[nuxt-feathers-zod] Feathers-Pinia is enabled but no Pinia instance was found (nuxtApp.$pinia is undefined). Install '@pinia/nuxt' in your Nuxt app modules, or set \`feathers.client.pinia = false\`. Falling back to the raw Feathers client for \`$api\`.")
    }
    else {
      piniaClient = createPiniaClient(feathersClient, {
        ssr: cm === 'remote' ? false : !!import.meta.server,
        ...piniaOptions as CreatePiniaClientConfig,
        pinia: (nuxt as any).$pinia,
      })
    }
  }
  `, `const piniaClient: any = undefined`)}

  // In remote mode with Pinia enabled, expose the Pinia client as $api and the raw Feathers client as $feathersClient.
  // This mirrors the usual Nuxt + feathers-pinia setup: transport/auth first, then createPiniaClient(feathersClient).
  const api = piniaClient || feathersClient

  // If we're using feathers-pinia, wrap api.service() lightly so transport errors stay readable.
  if (api && typeof (api as any).service === 'function') {
    const _apiService = (api as any).service.bind(api)
    ;(api as any).service = (path: any) => {
      const svc = _apiService(path)
      if (!svc || typeof svc !== 'object' || (svc as any).__nfz_api_wrapped)
        return svc

      const wrap = (method: string) => {
        const fn = (svc as any)[method]
        if (typeof fn !== 'function') return
        ;(svc as any)[method] = async (...args: any[]) => {
          try {
            return await fn.apply(svc, args)
          }
          catch (e: any) {
            throw normalizeError(e)
          }
        }
      }

      for (const m of ['find', 'get', 'create', 'update', 'patch', 'remove'])
        wrap(m)

      ;(svc as any).__nfz_api_wrapped = true
      return svc
    }
  }

  return {
    provide: {
      api,
      client: feathersClient,
      feathersClient,
      piniaClient,
    },
  }
})
`
  }
}
